var angkot = 1,
  nomorangkot = 6,
  jumlahangkot = 10;
for (angkot = angkot; angkot <= jumlahangkot; angkot++) {
  if (angkot <= nomorangkot) {
    console.log("Angkot No. " + angkot + " beroprasi dengan baik.");
  } else {
    console.log("Angkot No. " + angkot + " sedang tidak beroprasi.");
  }
}
